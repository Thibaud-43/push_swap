#include "libft.h"

char	*ft_strdup(const char *s)
{
	char		*dst;
	const int	slen = ft_strlen((char*)s);
	int			i;

	i = 0;
	if (!(dst = malloc(sizeof(char) * (slen + 1))))
		return (NULL);
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
